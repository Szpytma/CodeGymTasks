/* 
Zobaczyć dolary w przyszłości
*/

import java.io.*;
//
public class Solution {
    public static void main(String[] args) {
        //tutaj wpisz swój kod
        int i=1;
        while (i<11) {
            int j=1;
            while (j<11) {
                System.out.print("$");
                j++;
            }
            System.out.println();
            i++;
        }

    }
}
